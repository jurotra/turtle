import turtle
s= turtle.Screen()
mel= turtle.Turtle()
mel.shape("turtle")
adam= turtle.Turtle()
adam.shape("turtle")

mel.goto(10,50)
adam.goto(-10, 50)
mel.pu()
adam.pu()
mel.goto(50,70)
adam.goto(-50,70)
mel.pd()
adam.pd()
mel.home()
adam.home()
mel.fd(30)
adam.bk(30)
mel.goto(50, 70)
adam.goto(-50,70)
adam.pu()
mel.pu()
mel.home()
adam.home()

turtle.mainloop()